package org.example;

public class MatematicFunktion {
    public int addition (int a, int b) {
        int result = a + b;
        return result;
    }

    public int subtraktion (int a, int b) {
        int result = a - b;
        return result;
    }

    public int multiply (int a, int b) {
        int result = a * b;
        return result;
    }

    public int divide (int a, int b) {
        int result = a / b;
        return result;
    }
}